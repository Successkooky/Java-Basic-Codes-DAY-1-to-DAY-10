public class JavaBasics8 {
    public static void main(String args[])
    {
        //Relational operators
        int A=10;
        int B=14;
        System.out.println(A==B);//Returns False
        System.out.println(A!=B);//Return True
        System.out.println(A>B);//Returns False
        System.out.println(A<B);
        System.out.println(A<=B);
        System.out.println(A>=B);


        
    }
}
